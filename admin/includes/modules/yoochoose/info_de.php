<h2>YOOCHOOSE Empfehlungen für Gambio Online-Shops</h2>

<h3>Steigern Sie Ihren Umsatz</h3>
<p class='info_content'>
Führende Online-Shops haben schon seit Langem das Potenzial von professionellen Empfehlungen erkannt und 
systematisch genutzt. Erfahrungen bei anderen Online-Shops zeigen uns, dass - je nach Qualität der 
Empfehlungen - eine <strong>Umsatzsteigerung bis zu 20%</strong> realistisch ist. 

<h3>Bessere Kundenbindung</h3>
<p class='info_content'>
Käufer erwarten gute Empfehlungen in Ihrem Shop. Sie unterstützen den Käufer damit bei dem Auffinden des 
geeigneten Produktes und der Kaufentscheidung. Im Ergänzung zu einer Suche zeigen Empfehlungen auch Artikel, 
<strong>von denen der Käufer noch nicht wusste</strong>, dass sie im Shop erhältlich sind. Mit einem guten 
Empfehlungsdienst verbessern sie die Usability und Professionalität des Online Shops für Ihre Kunden.

<p class='info_content'>
Machen Sie YOOCHOOSE Empfehlungen zu Ihrem besten und erfolgreichsten Vertriebsmitarbeiter im Online-Shop.

<h2>Neue und verbesserte Funktionen</h2>

<h3>Startseite</h3>
<p class='info_content'>
<strong>Personalisierte Empfehlungen - "Empfehlungen für Sie" / Bestseller.</strong> Präsentieren Sie Ihren 
Kunden auf der Startseite personalisierte Empfehlungen, die auf Basis des Klick-, Kauf- und Nutzerverhaltens 
jedes einzelnen Kunden berechnet werden. Alternativ können Sie auch die meistverkauften Artikel anzeigen.

<h3>Kategorie-Seiten</h3>
<p class='info_content'>
<strong>"Die Bestseller in dieser Kategorie..."</strong> Bestseller sind die größten Umsatztreiber, aber die 
Bestseller-Funktion des Gamio Shop-Systems liefert diese nur global. Mit dieser Funktion können Sie Bestseller 
auch Kategorie bezogen darstellen lassen und damit in jeder Kategorie die Umsatztreiber in den Vordergrund 
stellen.

<h3>Warenkorb</h3>
<p class='info_content'>
<strong>"Das könnte Sie auch interessieren."</strong> Steigern Sie die Bestellmenge durch Empfehlungen im 
Warenkorb. Diese Funktion empfiehlt Ihren Kunden im Warenkorb Artikel, die oft zusammen mit den Artikeln, 
die bereits im Warenkorb liegen, gekauft wurden.

<h3>Artikel-Detail-Seite</h3>
<p class='info_content'>
<strong>Verbesserte "Kunden kauften auch..."-Funktion</strong> Die vorhandene Routine in Gambio zu 
"Kunden, welche diesen Artikel bestellten, haben auch folgende Artikel gekauft" wird durch eine deutlich 
perfomantere Funktion ersetzt, welche die zeitliche Abfolge der Klick- und Kaufereignisse berücksichtigt.

<p class='info_content'>
<strong>"Kunden, die sich für dieses Produkt interessierten, haben sich auch für folgende Produkte 
interessiert..."</strong> Diese Funktion ist auch für kleine Shops besonders interessant, da die wesentlich 
häufigeren Klick-Ereignisse benutzt werden, um Produktempfehlungen zu erstellen. Sie erscheint bei Ihnen 
in einer zusätzlichen Box im Layout. 
